#include "libft.h"

char	*ft_strchr(const char *str, int c)
{
	int	i;

	i = 0;
	if (str == NULL)
		return (NULL);
	while (str != 0)
	{
		if (str[i] == c)
			return ((char *) str + i);
		i++;
	}
	return (NULL);
}
